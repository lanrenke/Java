package code.Base01.Base09;
/**
 目标：研究运算符。
 什么是运算符：
 是对常量和变量进行运算的符号。
 运算符在表达式中经常使用。

 Java的运算符有哪些呢？
 运算符的分类:
 1.算数运算符
 + - * / % ++ --
 -- 基本的算术运算符：+ - * / %
 小结：除法是/   求余数是%（取模）  乘法*

 */
public class OperatorDemo01 {

    public static void main(String[] args) {

        int a = 10;
        int b = 2;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        System.out.println("-----------------");

        int c = 10;
        int d = 3;
        System.out.println(c + d);
        System.out.println(c - d);
        System.out.println(c * d);
        System.out.println(c / d);
        System.out.println(c % d);

    }

}
