package code.Base01.Base01;

public class Constants03 {

    public static void main(String[] args) {

        // 输出这些常量
        //整数常量： 100 , 0  , 2
        System.out.println(100);
        System.out.println(0);
        System.out.println(2);
        System.out.println("--------------------");

        //小数常量:  99.9  , 0.001
        System.out.println(99.9);
        System.out.println(0.001);
        System.out.println("--------------------");

        /*字符常量： 'a' , '1' , '中' , '中'
			-- 只能是单个字符
			-- 必须用单引号围起来*/
        System.out.println('a');
        System.out.println('1');
        System.out.println('中');
        System.out.println("--------------------");

        /*
		字符串常量："a" ,"", "我爱你","1221313dsaaa你"
		    -- 必须用双引号围起来 */
        System.out.println("a");
        System.out.println("i love you");
        System.out.println("--------------------");

        /*
		布尔常量：
		    -- 只有两个值 真true , 假false */
        System.out.println(false);
        System.out.println(true);


    }

}
