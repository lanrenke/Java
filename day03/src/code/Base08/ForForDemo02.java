package code.Base08;
/**
 目标：嵌套循环输出3行4列星星。

 ****
 ****
 ****

 */
public class ForForDemo02 {

    public static void main(String[] args) {

        for (int i = 0; i < 3; i++) {

            for (int j = 0; j < 4; j++) {

                System.out.print("*");

            }
            System.out.println();

        }

    }

}
